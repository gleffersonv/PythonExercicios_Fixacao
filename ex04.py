digite=input('Digite qualquer coisa? ')
print('Qual o tipo primitivo desse valor? ', type(digite))
print('Isto é uma string ?', digite.isalpha())
print('Isto é um numero ?', digite.isnumeric())
print('Isto é um Alfanumerico? ', digite.isalnum())
print('Ele esta em maisculo? ', digite.isupper())
print('Ele esta em minusculo? ', digite.islower())

